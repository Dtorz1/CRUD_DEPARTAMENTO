    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        if (txtIdDepartment.getText().isEmpty() || txtNombre.getText().isEmpty() || txtManager.getText().isEmpty()
                || txtLocation.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Llene todos los campos");
        }else{
            Department dpt = new Department();
            dpt.setDepartmentId(Integer.parseInt(txtIdDepartment.getText()));
            dpt.setDepartmentName(txtNombre.getText());
            dpt.setManagerId(Integer.parseInt(txtManager.getText()));
            dpt.setLocationId(Integer.parseInt(txtLocation.getText()));
            String mensaje = dbo.agregarDepartment(dpt);
            JOptionPane.showMessageDialog(null, mensaje);
            limpiar();
            mostrarDepartment();
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void tbDepartmentMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbDepartmentMouseClicked
       int seleccion = tbDepartment.rowAtPoint(evt.getPoint());
         txtIdDepartment.setText(tbDepartment.getValueAt(seleccion, 0)+"");
         txtNombre.setText(tbDepartment.getValueAt(seleccion, 1)+"");
         txtManager.setText(tbDepartment.getValueAt(seleccion, 2)+"");
         txtLocation.setText(tbDepartment.getValueAt(seleccion, 3)+"");
    }//GEN-LAST:event_tbDepartmentMouseClicked
public void limpiar(){
         txtIdDepartment.setText("");
         txtNombre.setText("");
         txtManager.setText("");
         txtLocation.setText("");
        
         idMax();
}
    
