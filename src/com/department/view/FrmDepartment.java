    private void BtnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnEliminarActionPerformed
               if (txtIdDepartment.getText().isEmpty() || txtNombre.getText().isEmpty() || txtManager.getText().isEmpty()
                || txtLocation.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Llene todos los campos");
        }else{

            String mensaje = dbo.borrarDepartment(Integer.parseInt(txtIdDepartment.getText()));
            JOptionPane.showMessageDialog(null, mensaje);
            limpiar();
            mostrarDepartment();
        }
    }//GEN-LAST:event_BtnEliminarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
          if (txtIdDepartment.getText().isEmpty() || txtNombre.getText().isEmpty() || txtManager.getText().isEmpty()
                || txtLocation.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Llene todos los campos");
        }else{
            Department dpt = new Department();
            dpt.setDepartmentId(Integer.parseInt(txtIdDepartment.getText()));
            dpt.setDepartmentName(txtNombre.getText());
            dpt.setManagerId(Integer.parseInt(txtManager.getText()));
            dpt.setLocationId(Integer.parseInt(txtLocation.getText()));
            String mensaje = dbo.modificarDepartment(dpt);
            JOptionPane.showMessageDialog(null, mensaje);
            limpiar();
            mostrarDepartment();
        }
    }//GEN-LAST:event_btnModificarActionPerformed

